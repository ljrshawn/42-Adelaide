#include <unistd.h>

void	ft_putchar(int a, char node, char side)
{
	write(1, &node, 1);
	while (a > 2)
	{
		write(1, &side, 1);
		a--;
	}
	if (a > 1)
	{
		write(1, &node, 1);
	}
	write(1, "\n", 1);
}
