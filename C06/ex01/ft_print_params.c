#include <unistd.h>

int	main(int args, char *argv[])
{
	int	i;
	int	j;

	j = 1;
	if (args > 1)
	{
		while (j < args)
		{
			i = 0;
			while (argv[j][i] != '\0')
			{
				write(1, &argv[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j++;
		}
	}
}
